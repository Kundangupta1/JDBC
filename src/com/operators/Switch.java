package com.operators;

public class Switch {

	public static void main(String[] args) {
		int num = 800;
		
		switch(num) {
		case 100: System.out.println("Case 1");
		break;
		case 400: System.out.println("Case 2");
		break;
		case 300: System.out.println("Case 3");
		break;
		case 500: System.out.println("Case 4");
		break;
		default: System.out.println("No case are matching");
		}
	}
}
